#include "philo.h"

int		ft_atoi(const char *str)
{
	int		i;
	long	res;

	i = 0;
	res = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 0);
		if (res > 2147483647)
			return (-1);
		i++;
	}
	return ((int)res);
}